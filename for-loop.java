public class Main {
    public static void main(String[] args) {

        /*
        * for loop = executes a block of code a limited amount of times
        */

//        ascending  0 => 10
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

//        descending  10 => 0

        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
        System.out.println("Happy new year!");

//        if we need to cou nt down by two
      
        for (int i = 10; i >= 0; i-=2) {
            System.out.println(i);
        }

    }
}
